void updateArray(char displayValues[6][4][21], char digitValues[4][21], char colPosition) {
    char row, byteValue;
    for (row = 1; row < 5; row++) {
        for (byteValue = 0; byteValue < 21; byteValue++) {
            displayValues[row][colPosition][byteValue] = digitValues[row-1][byteValue];
        };
    };
};

